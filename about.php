<h1><span class="th-color">Psycho-Oncology Society of Nigeria </span>(POSON)</h1>
<p>The Psycho-Oncology Society of Nigeria (POSON) was registered with the Corporate Affairs Commission (CAC) in Nigeria on the 24th of April, 2009 with one of its cardinal objectives being that of offering psychosocial services to cancer patients and their caregivers in Nigeria and other African countries.  Also, the organization seeks to provide a training ground to further the work of Psycho-Oncology in Nigeria, to conduct research in the area of Psycho-Oncology, to act as patients advocate and to raise funds to assist patients when necessary. POSON was inaugurated on the 6th of October 2011 in Ibadan and became a member of the Federation of Psycho-Oncology Societies in 2011 at the International Psycho-Oncology Society (IPOS) conference in Turkey.</p>
